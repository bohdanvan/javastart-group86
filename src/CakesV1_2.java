public class CakesV1_2 {

    public static void main(String[] args) {
        int cakes = 10;

        System.out.println("I have " + cakes + " cakes");
        for (int n = 1; n <= cakes; n++) {
            System.out.println("I have eaten " + n + " cake");
        }
    }
}
